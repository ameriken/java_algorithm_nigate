package saiki.gcd;

public class Practice {
    public static Long gcd(Long a, Long b) {
        // ここにこーどを書く
        return (Long)1L;
    }


    public static void main(String[] args) {
        //コード
        Long a = 3L;
        Long b = 15L;

        Long sum = gcd(a, b);

        // 最大公倍数
        System.out.println(sum);

        // (与えられた整数 * 与えられた) / 整数最大公倍数 = 最小公倍数
    }
}
